    private void bt_XacNhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_XacNhanMouseClicked
        //lấy dữ liệu
        String id_GT = tf_id.getText();
        String ten = tf_ten.getText();
        String mota = tf_mt.getText();
        
        String giaTien = tf_giat.getText();

        // Kiểm tra các trường không được để trống
        if (id_GT.isEmpty() || ten.isEmpty() || mota.isEmpty() || giaTien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống thông tin", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        

        
        Double giaT;
        try {
            giaT = Double.parseDouble(giaTien);
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this, "Giá thuê nhập vào phải là kiểu số.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (GoiTapDAO.getInstance().selectById(id_GT) != null) {
            JOptionPane.showMessageDialog(this, "Gói tập đã tồn tại", "Cảnh báo!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        

        GOITAP gt = new GOITAP(id_GT, ten, mota, giaT);

        try {

            GoiTapDAO.getInstance().insert(gt);
            panel.loadDataToTable(GoiTapDAO.getInstance().selectAll());
            JOptionPane.showMessageDialog(this, "Thêm gói tập thành công!");
            this.dispose();
        } catch (RuntimeException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_bt_XacNhanMouseClicked

